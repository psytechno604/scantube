int AddressProvider::getPseudoIndex(int unitTndex)
{
    try {
        auto keys = s_pseudoIndexToIndexTable.keys();
        foreach(auto key, keys) {
            if (s_pseudoIndexToIndexTable[key] == unitTndex) {
                return int(key);
            }
        }
        return -1;
    }
    catch (...) {
        return -1;
    }
}
